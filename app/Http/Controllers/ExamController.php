    public function setAbstract(Request $request, $id){
        $exam = Exam::find($id);
        $exam->abstract = $request->all()['abstract'];
        $time = Carbon::parse($exam->initial_date)->diffInSeconds(Carbon::now());
        $exam->time=$time;
        $exam->save();
        return response()->json($exam);
    }

